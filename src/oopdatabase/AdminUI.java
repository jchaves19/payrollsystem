    //generate payroll
    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        AdminAccount admin = new AdminAccount();
        ResultSet  a  = admin.generatePayroll();
        LinkedList b  = admin.calculate();
        jTextArea1.setText("Name                                    Regular Hours   Non-Regular Hours       Wage");
        
        try {
            int index = 0;
            while(a.next()){
                if (index == b.size()){
                    break;
                }
                jTextArea1.append("\n" +
                a.getObject("employee.Name") + "                    " + a.getObject("hourswork.Regular") + "                                " + a.getObject("hourswork.NonRegular")+ "                       " + b.get(index)
                );  
                index++;    
            }
        } catch (SQLException ex) {
            Logger.getLogger(AdminUI.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        admin.clear();
    }//GEN-LAST:event_jButton6ActionPerformed

