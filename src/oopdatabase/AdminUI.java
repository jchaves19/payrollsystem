    /**
     * This method returns nothing and has a behavior 
     * to add employee.
     */
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        AdminAccount admin = new AdminAccount();
        String sex = null;
        
        if(jCheckBox1.isSelected()){
            sex = "Male";
        }else if(jCheckBox2.isSelected()){
            sex = "female";
        }
                
        try {
            if(admin.addEmployee(jTextField1.getText(), jTextField2.getText(), jTextField3.getText(), jTextField4.getText(),
            jTextField5.getText(), jTextField7.getText(), sex, jTextField6.getText()) == true){
                Password pass = new Password();
                String id = pass.addPassword(jTextField1.getText());
                ResultSet rs = admin.viewEmployee(id);
                
                while (rs.next()){
                    jTextArea1.setText(
                            "Name:" + rs.getObject("Name") + "\n" 
                            +"Address:" + rs.getObject("Address") + "\n"
                            +"Contact Number:" + rs.getObject("ContactNumber") + "\n"
                            +"Emergency Contact Number:" + rs.getObject("EmergencyNum") + "\n"
                            +"Sex:" + rs.getObject("Sex") + "\n"
                            +"Birth Date:" + rs.getObject("BirthDate") + "\n"
                            +"Rate:" + rs.getObject("Rate") + "\n"
                            +"Department:" + rs.getObject("DeptName") + "\n"
                            +"Hours Worked Regular:" + rs.getObject("Regular") + "\n"
                            +"Hours Worked Non-Regular:" + rs.getObject("NonRegular") + "\n"
                            +"Current Wage:" + rs.getObject("WageAmount") + "\n"
                    );
                }
            }
        } catch (SQLException ex) {
            Logger.getLogger(AdminUI.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

