    //view employee
    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        AdminAccount admin = new AdminAccount();
        
        ResultSet rs = admin.viewEmployee(JOptionPane.showInputDialog("Input employee id"));
                
        try {
            while (rs.next()){
                jTextArea1.setText(
                        "Name:" + rs.getObject("Name") + "\n"
                        +"Address:" + rs.getObject("Address") + "\n"
                        +"Contact Number:" + rs.getObject("ContactNumber") + "\n"
                        +"Emergency Contact Number:" + rs.getObject("EmergencyNum") + "\n"
                        +"Sex:" + rs.getObject("Sex") + "\n"
                        +"Birth Date:" + rs.getObject("BirthDate") + "\n"
                        +"Rate:" + rs.getObject("Rate") + "\n"
                        +"Department:" + rs.getObject("DeptName") + "\n"
                        +"Hours Worked Regular:" + rs.getObject("Regular") + "\n"
                        +"Hours Worked Non-Regular:" + rs.getObject("NonRegular") + "\n"
                        +"Current Wage:" + rs.getObject("WageAmount") + "\n"
                );
            }
        } catch (SQLException ex) {
            Logger.getLogger(AdminUI.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton4ActionPerformed

